	<div class="container">
		<h5>1. Be Respectful & Courteous</h5>
		<ul>
			<li>Treat all members with respect. No harassment, bullying, or personal attacks.</li>
			<li>Disagreements are fine, but stay civil and constructive.</li>
		</ul>

		<h5>2. Stay On Topic</h5>
		<ul>
			<li>Keep discussions relevant to the forum’s purpose.</li>
			<li>If your post doesn’t fit an existing category, use the General Discussion forum.</li>
		</ul>

		<h5>3. No Hate Speech or Discrimination</h5>
		<ul>
			<li>No posts that promote racism, sexism, homophobia, or any other form of discrimination.</li>
		</ul>

		<h5>4. No Spam or Self-Promotion</h5>
		<ul>
			<li>No excessive posting of advertisements, affiliate links, or promotions.</li>
			<li>Ranch owners may post about services, but excessive self-promotion is not allowed.</li>
		</ul>

		<h5>5. Protect Privacy & Personal Information</h5>
		<ul>
			<li>Do not share personal details (yours or others) such as addresses, phone numbers, or emails.</li>
			<li>Avoid discussing ongoing legal disputes in the forum.</li>
		</ul>

		<h5>6. Follow Ranch Rules & Local Laws</h5>
		<ul>
			<li>Ensure discussions align with Ranch bylaws and regulations.</li>
			<li>No posts encouraging illegal activity.</li>
		</ul>

		<h5>7. No Misinformation or False Claims</h5>
		<ul>
			<li>Do not spread rumors or unverified information about the HOA, residents, or board decisions.</li>
		</ul>

		<h5>8. No Duplicate or Off-Topic Posts</h5>
		<ul>
			<li>Search before posting to avoid duplicates.</li>
			<li>Off-topic posts may be removed or moved to a better section.</li>
		</ul>

		<h5>9. Moderation & Enforcement</h5>
		<ul>
			<li>The Board reserves the right to edit, move, or remove posts that violate guidelines.</li>
			<li>Repeated violations may lead to temporary or permanent bans.</li>
		</ul>

		<h5>10. Report Issues Instead of Engaging</h5>
		<ul>
			<li>If you see inappropriate content, use the Report function instead of escalating the situation.</li>
		</ul>
